def get_translations_dict(lang_code: str) -> dict:
    """Convert the list-based translations to a dictionary."""
    if lang_code not in LANGUAGE_TRANSLATIONS:
        return {}

    translations_list = LANGUAGE_TRANSLATIONS[lang_code]
    if len(translations_list) != len(SOURCE_STRINGS):
        print(f"Warning: Translation count mismatch for {lang_code}")
        print(f"  Expected: {len(SOURCE_STRINGS)}, Got: {len(translations_list)}")
        return {}

    return dict(zip(SOURCE_STRINGS, translations_list))
